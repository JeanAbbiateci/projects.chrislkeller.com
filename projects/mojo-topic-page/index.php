<h2>The Latest</h2>
<p>A <a href"#">Dane County</a> judge struck down <a href"#">Gov. Scott Walker</a>'s controversial collective bargaining law on Thursday, ruling that a legislative committee violated the state's open meetings law when it hastily convened to amend the measure.</p>
<p>The <a href="#">ruling</a> by <a href"#">Dane County Circuit Judge Maryann Sumi</a> won't end the legal fight over the law, which sharply curtails the collective bargaining rights of most public employees in <a href="#">Wisconsin.</a></p>
<p>An appeal of Sumi's decision is all but certain. And the <a href="#">state Supreme Court</a> is set to hear oral arguments on June 6 over whether to take the case immediately in response to an earlier request by the state <a href="#">Department of Justice</a> and state <a href="#">Department of Administration</a> challenging Sumi's authority to act in the case.</p>
<p>The high court also has yet to rule on an appeal by DOJ lawyers that was filed after Sumi issued a <a href="#">restraining order in March barring implementation of the law</a>.</p>
