<img src="img/band.jpg" alt="Work Awesome" width="493px">
<p>Lemonpickels is a Florida-based band who've amassed a huge global following on YouTube, with over 170 million video views so far. 
</p><p>Though they have a silly name, Lemonpickels is a real band* with a real sense of musicality - they play their own music, write all their own songs and consider themselves real music connoisseurs.</p> 
<p>The band started in 2001 when Kyle Marshall and Ricky Cromroot met in line at a grocery store. Ricky was humming "Always" from Bon Jovi, and soon they began singing out loud together. After a few short versus sung out to the benefit of the grocery shoppers, the two laughed and hugged while the shocked audience applauded. It was the start of something big.</p>
<p>A few months later, the boys added guitarist Spencer Blade and drummer Leonardo Fransisco.</p> 
<p>Now, the band is famous throughout the world for their smooth style, original lyrics, and impromptu grocery store concerts.</p>
